package henu.rjxy.bean;

public class Study {
		private int stid;
		private int userId;
		private double sty_time;
		private String test_questions;
		private String word;
		public int getStid() {
			return stid;
		}
		public void setStid(int stid) {
			this.stid = stid;
		}
		public int getUserId() {
			return userId;
		}
		public void setUserId(int userId) {
			this.userId = userId;
		}
		public double getSty_time() {
			return sty_time;
		}
		public void setSty_time(double sty_time) {
			this.sty_time = sty_time;
		}
		public String getTest_questions() {
			return test_questions;
		}
		public void setTest_questions(String test_questions) {
			this.test_questions = test_questions;
		}
		public String getWord() {
			return word;
		}
		public void setWord(String word) {
			this.word = word;
		}
		
}
